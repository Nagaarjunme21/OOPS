package Inheritance;

public class Analog {
	
	public void displayTime(String watchType) {
		System.out.println(watchType + " 10am");
	}
	
	public void displayDate(String watchType) {
		System.out.println(watchType + " 01/11/2022");
	}
	
	public void displayDay(String watchType) {
		System.out.println(watchType + " Tuesday");
	}

}
